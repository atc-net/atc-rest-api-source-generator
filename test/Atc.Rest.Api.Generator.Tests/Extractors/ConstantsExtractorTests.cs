namespace Atc.Rest.Api.Generator.Tests.Extractors;

public class ConstantsExtractorTests
{
    [Theory]
    [InlineData("MontaPartner.ApiClient", "Monta-ApiClient")]
    [InlineData("Demo", "Demo-ApiClient")]
    [InlineData("NexusSample.ApiClient", "NexusSample-Custom")]
    public void Generate_ContainsCorrectHttpClientName(
        string projectName,
        string effectiveHttpClientName)
    {
        // Act
        var result = ConstantsExtractor.Generate(projectName, effectiveHttpClientName);

        // Assert
        Assert.Contains($"public const string HttpClientName = \"{effectiveHttpClientName}\";", result, StringComparison.Ordinal);
    }

    [Fact]
    public void Generate_ContainsCorrectNamespace()
    {
        // Act
        var result = ConstantsExtractor.Generate("MontaPartner.ApiClient", "Monta-ApiClient");

        // Assert
        Assert.Contains("namespace MontaPartner.ApiClient.Generated;", result, StringComparison.Ordinal);
    }

    [Fact]
    public void Generate_ContainsAutoGeneratedHeader()
    {
        // Act
        var result = ConstantsExtractor.Generate("Demo", "Demo-ApiClient");

        // Assert
        Assert.StartsWith("// <auto-generated />", result, StringComparison.Ordinal);
    }

    [Fact]
    public void Generate_ContainsStaticClass()
    {
        // Act
        var result = ConstantsExtractor.Generate("Demo", "Demo-ApiClient");

        // Assert
        Assert.Contains("public static class Constants", result, StringComparison.Ordinal);
    }
}