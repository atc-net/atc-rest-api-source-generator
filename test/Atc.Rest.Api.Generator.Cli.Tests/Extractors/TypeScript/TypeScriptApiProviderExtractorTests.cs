namespace Atc.Rest.Api.Generator.Cli.Tests.Extractors.TypeScript;

public class TypeScriptApiProviderExtractorTests
{
    [Fact]
    public void GenerateApiProvider_EmitsRefTrackingBothBaseUrlAndOptions()
    {
        var result = TypeScriptApiProviderExtractor.GenerateApiProvider(headerContent: null);

        // Both baseUrl AND options must be tracked so auth header / token-provider updates
        // propagate to a freshly constructed ApiService instead of using the stale one.
        Assert.Contains("serviceRef.current.baseUrl !== baseUrl", result, StringComparison.Ordinal);
        Assert.Contains("serviceRef.current.options !== options", result, StringComparison.Ordinal);
        Assert.Contains("new ApiService(baseUrl, options)", result, StringComparison.Ordinal);
    }

    [Fact]
    public void GenerateApiProvider_ImportsUseRefNotUseMemo()
    {
        var result = TypeScriptApiProviderExtractor.GenerateApiProvider(headerContent: null);

        Assert.Contains("import { createContext, useRef, createElement } from 'react';", result, StringComparison.Ordinal);
        Assert.DoesNotContain("useMemo", result, StringComparison.Ordinal);
    }

    [Fact]
    public void GenerateApiProvider_PrependsHeaderContentWhenProvided()
    {
        const string header = "// <auto-generated />\n";

        var result = TypeScriptApiProviderExtractor.GenerateApiProvider(header);

        Assert.StartsWith(header, result, StringComparison.Ordinal);
    }

    [Fact]
    public void GenerateUseApiService_EmitsContextGuard()
    {
        var result = TypeScriptApiProviderExtractor.GenerateUseApiService(headerContent: null);

        Assert.Contains("useContext(ApiServiceContext)", result, StringComparison.Ordinal);
        Assert.Contains("must be used within an <ApiProvider>", result, StringComparison.Ordinal);
    }
}