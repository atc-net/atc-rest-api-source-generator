namespace Atc.Rest.Api.Generator.Cli.Tests.Extractors.TypeScript;

public class TypeScriptHeaderBuilderTests
{
    [Fact]
    public void AutoGeneratedHeader_StartsWithGeneratedCommentMarker()
    {
        // The marker is what every emitter prepends to gate scaffold and regen behavior.
        // Changing it silently breaks downstream tools that check for the marker.
        Assert.StartsWith("// <auto-generated", TypeScriptHeaderBuilder.AutoGeneratedHeader, StringComparison.Ordinal);
    }

    [Fact]
    public void AutoGeneratedHeader_EndsWithNewline()
    {
        // Subsequent emitter code expects to append directly without re-adding a separator.
        Assert.EndsWith("\n", TypeScriptHeaderBuilder.AutoGeneratedHeader, StringComparison.Ordinal);
    }

    [Fact]
    public void AutoGeneratedHeader_IsStableAcrossCalls()
    {
        // It is exposed as a property; ensure we always get the same identity-equivalent string.
        Assert.Same(TypeScriptHeaderBuilder.AutoGeneratedHeader, TypeScriptHeaderBuilder.AutoGeneratedHeader);
    }
}