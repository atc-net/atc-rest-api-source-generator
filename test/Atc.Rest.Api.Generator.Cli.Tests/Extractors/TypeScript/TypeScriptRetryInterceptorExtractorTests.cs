namespace Atc.Rest.Api.Generator.Cli.Tests.Extractors.TypeScript;

public class TypeScriptRetryInterceptorExtractorTests
{
    [Fact]
    public void Generate_ImportsRetryPolicyType()
    {
        var result = TypeScriptRetryInterceptorExtractor.Generate(headerContent: null);

        Assert.Contains("import type { RetryPolicy } from './retryConfig';", result, StringComparison.Ordinal);
    }

    [Fact]
    public void Generate_EmitsSleepHelperWithAbortSupport()
    {
        // Cancellation must propagate through the sleep so retries respect AbortSignal.
        var result = TypeScriptRetryInterceptorExtractor.Generate(headerContent: null);

        Assert.Contains("function sleep(ms: number, signal?: AbortSignal)", result, StringComparison.Ordinal);
        Assert.Contains("signal?.addEventListener('abort'", result, StringComparison.Ordinal);
    }

    [Fact]
    public void Generate_ComputeDelay_HandlesAllBackoffStrategies()
    {
        var result = TypeScriptRetryInterceptorExtractor.Generate(headerContent: null);

        Assert.Contains("case 'constant'", result, StringComparison.Ordinal);
        Assert.Contains("case 'linear'", result, StringComparison.Ordinal);
        Assert.Contains("case 'exponential'", result, StringComparison.Ordinal);
    }

    [Fact]
    public void Generate_HeaderContent_PrependedWhenProvided()
    {
        const string header = "// <auto-generated />\n";

        var result = TypeScriptRetryInterceptorExtractor.Generate(header);

        Assert.StartsWith(header, result, StringComparison.Ordinal);
    }
}