// ReSharper disable InvertIf
namespace Atc.Rest.Api.Generator.Extractors;

/// <summary>
/// Extracts webhook responses and converts them to result classes.
/// Webhook results are simpler than regular API results - they typically just return
/// acknowledgement status codes (200 OK, 202 Accepted).
/// </summary>
public static class WebhookResultExtractor
{
    /// <summary>
    /// Extracts webhook result classes from OpenAPI document.
    /// </summary>
    /// <param name="openApiDoc">The OpenAPI document containing webhook definitions.</param>
    /// <param name="projectName">The name of the project (used for namespace).</param>
    /// <param name="includeDeprecated">Whether to include deprecated webhooks.</param>
    /// <returns>List of generated result class code strings.</returns>
    public static List<(string ClassName, string Content)>? Extract(
        OpenApiDocument openApiDoc,
        string projectName,
        bool includeDeprecated = false)
    {
        if (openApiDoc == null)
        {
            throw new ArgumentNullException(nameof(openApiDoc));
        }

        if (!openApiDoc.HasWebhooks())
        {
            return null;
        }

        var resultsList = new List<(string ClassName, string Content)>();
        var namespaceValue = NamespaceBuilder.ForWebhookResults(projectName);

        foreach (var (webhookName, httpMethod, operation) in openApiDoc.GetAllWebhookOperations())
        {
            // Skip deprecated webhooks if not including them
            if (!includeDeprecated && operation.Deprecated)
            {
                continue;
            }

            var result = GenerateWebhookResultClass(
                webhookName,
                operation,
                namespaceValue);

            if (result != null)
            {
                resultsList.Add(result.Value);
            }
        }

        return resultsList.Count > 0 ? resultsList : null;
    }

    private static (string ClassName, string Content)? GenerateWebhookResultClass(
        string webhookName,
        OpenApiOperation operation,
        string namespaceValue)
    {
        // Get operation ID or generate from webhook name
        var operationId = operation.OperationId ?? $"On{webhookName.EnsureFirstCharacterToUpper()}";
        var className = $"{operationId.EnsureFirstCharacterToUpper()}WebhookResult";

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System.CodeDom.Compiler;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using Microsoft.AspNetCore.Http;");
        sb.AppendLine();
        sb.AppendLine($"namespace {namespaceValue};");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine($"/// Result type for the {webhookName} webhook handler.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine($"[GeneratedCode(\"{GeneratorInfo.Name}\", \"{GeneratorInfo.Version}\")]");
        sb.AppendLine($"public sealed class {className} : IResult");
        sb.AppendLine("{");
        sb.AppendLine(4, "private readonly IResult innerResult;");
        sb.AppendLine();
        sb.AppendLine(4, $"private {className}(IResult innerResult)");
        sb.AppendLine(4, "{");
        sb.AppendLine(8, "this.innerResult = innerResult;");
        sb.AppendLine(4, "}");

        // Generate factory methods based on responses defined in the webhook
        if (operation.Responses != null)
        {
            foreach (var response in operation.Responses)
            {
                var statusCode = response.Key;
                var responseObj = response.Value;

                if (responseObj == null)
                {
                    continue;
                }

                var methodName = GetFactoryMethodName(statusCode);
                var description = responseObj.Description ?? $"HTTP {statusCode} response";

                sb.AppendLine();
                sb.AppendLine(4, "/// <summary>");
                sb.AppendLine(4, $"/// {statusCode} - {description}");
                sb.AppendLine(4, "/// </summary>");
                sb.AppendLine(4, $"public static {className} {methodName}()");
                sb.AppendLine(8, $"=> new({GetTypedResultExpression(statusCode)});");
            }
        }
        else
        {
            // Default to 200 OK if no responses defined
            sb.AppendLine();
            sb.AppendLine(4, "/// <summary>");
            sb.AppendLine(4, "/// 200 OK - Webhook acknowledged.");
            sb.AppendLine(4, "/// </summary>");
            sb.AppendLine(4, $"public static {className} Ok()");
            sb.AppendLine(8, "=> new(TypedResults.Ok());");
        }

        sb.AppendLine();
        sb.AppendLine(4, "public Task ExecuteAsync(HttpContext httpContext)");
        sb.AppendLine(8, "=> innerResult.ExecuteAsync(httpContext);");
        sb.AppendLine("}");

        return (className, sb.ToString());
    }

    private static string GetFactoryMethodName(string statusCode)
        => statusCode switch
        {
            "200" => "Ok",
            "201" => "Created",
            "202" => "Accepted",
            "204" => "NoContent",
            "400" => "BadRequest",
            "401" => "Unauthorized",
            "403" => "Forbidden",
            "404" => "NotFound",
            "500" => "InternalServerError",
            "default" => "Error",
            _ when int.TryParse(statusCode, out var code) => $"StatusCode{code}",
            _ => "StatusCode",
        };

    /// <summary>
    /// Maps a webhook response status code to the C# expression used to build the inner
    /// <c>IResult</c> that the generated <c>*WebhookResult</c> class wraps.
    /// </summary>
    /// <remarks>
    /// <para>
    /// Webhooks are fire-and-forget acknowledgements from the subscriber back to the publisher.
    /// They do NOT have:
    /// </para>
    /// <list type="bullet">
    ///   <item><description>A location URI to return (so no <c>Created(uri)</c> or <c>Accepted(uri, value)</c>).</description></item>
    ///   <item><description>A response body / payload to serialize (so no <c>Ok(value)</c> / <c>Created(uri, value)</c>).</description></item>
    ///   <item><description>An authentication context on the result (so <c>Forbid()</c>, which is auth-scheme driven, is wrong for "403 Forbidden" as a webhook ack).</description></item>
    /// </list>
    /// <para>
    /// That means for most non-trivial status codes there is no idiomatic parameter-only
    /// <c>TypedResults.X()</c> overload whose semantics match a webhook ack. We deliberately
    /// emit <c>TypedResults.StatusCode(code)</c> in those cases, which mirrors the safe-fallback
    /// pattern in <c>ResultClassExtractor.cs</c> (see its <c>Created</c> handler: <c>uri != null
    /// ? TypedResults.Created(uri) : TypedResults.StatusCode(201)</c>).
    /// </para>
    /// <para>
    /// Per-code rationale:
    /// </para>
    /// <list type="bullet">
    ///   <item><description><c>200/204/400/401/404/409</c>: the parameterless <c>Ok()</c>/<c>NoContent()</c>/<c>BadRequest()</c>/<c>Unauthorized()</c>/<c>NotFound()</c>/<c>Conflict()</c> factories have exactly the "empty body, status X" semantics we want, so we use them.</description></item>
    ///   <item><description><c>201 Created</c>: <c>TypedResults.Created()</c> parameterless IS valid in .NET 10, but it implies "resource created" and callers often visually expect a location URI. We emit <c>StatusCode(201)</c> so the intent ("ack only, no Location header") is unambiguous.</description></item>
    ///   <item><description><c>202 Accepted</c>: all <c>TypedResults.Accepted</c> overloads expect either a location or a value; we have neither, so <c>StatusCode(202)</c> is the cleanest expression of "accepted, nothing to return".</description></item>
    ///   <item><description><c>403 Forbidden</c>: <c>TypedResults.Forbid()</c> is an auth-challenge result tied to authentication schemes, not a plain HTTP 403. For webhooks we just want the status code, so we use <c>StatusCode(403)</c>.</description></item>
    ///   <item><description><c>500</c>, <c>default</c>, and unknown codes: no dedicated <c>TypedResults.InternalServerError()</c> exists (and we don't want to tie default to auth or body semantics), so <c>StatusCode(code)</c> is used uniformly.</description></item>
    /// </list>
    /// </remarks>
    private static string GetTypedResultExpression(string statusCode)
        => statusCode switch
        {
            "200" => "TypedResults.Ok()",
            "201" => "TypedResults.StatusCode(201)",
            "202" => "TypedResults.StatusCode(202)",
            "204" => "TypedResults.NoContent()",
            "400" => "TypedResults.BadRequest()",
            "401" => "TypedResults.Unauthorized()",
            "403" => "TypedResults.StatusCode(403)",
            "404" => "TypedResults.NotFound()",
            "409" => "TypedResults.Conflict()",
            "500" => "TypedResults.StatusCode(500)",
            "default" => "TypedResults.StatusCode(500)",
            _ when int.TryParse(statusCode, out var code) => $"TypedResults.StatusCode({code.ToString(CultureInfo.InvariantCulture)})",
            _ => "TypedResults.Ok()",
        };
}