namespace Atc.Rest.Api.Generator.Extractors;

/// <summary>
/// Generates health check endpoint registration and DI extension methods.
/// Supports /health, /health/live, /health/ready with optional API key security.
/// </summary>
public static class HealthCheckExtractor
{
    /// <summary>
    /// Generates the health check endpoint registration code.
    /// </summary>
    /// <param name="projectName">The project name for namespace.</param>
    /// <param name="config">The health check configuration.</param>
    /// <returns>Generated C# source code for the endpoint registration class.</returns>
    public static string GenerateEndpoints(
        string projectName,
        HealthCheckConfig config,
        bool hasHealthChecksPackage = false)
    {
        var ns = NamespaceBuilder.BuildBase(projectName);
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using Microsoft.AspNetCore.Builder;");
        sb.AppendLine("using Microsoft.AspNetCore.Diagnostics.HealthChecks;");

        if (IsApiKeySecurity(config))
        {
            sb.AppendLine("using Microsoft.AspNetCore.Http;");
            sb.AppendLine("using Microsoft.Extensions.Configuration;");
            sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");
        }

        if (hasHealthChecksPackage)
        {
            sb.AppendLine("using Atc.Rest.HealthChecks.Factories;");
        }

        sb.AppendLine();
        sb.AppendLine($"namespace {ns}.Health;");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Extension methods for registering health check endpoints.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("public static class HealthCheckEndpoints");
        sb.AppendLine("{");
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Maps health check endpoints to the application.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static WebApplication MapHealthCheckEndpoints(this WebApplication app)");
        sb.AppendLine("    {");

        var path = config.Path.TrimEnd('/');

        if (IsApiKeySecurity(config))
        {
            AppendSecuredEndpoints(sb, path, config, hasHealthChecksPackage, projectName);
        }
        else
        {
            AppendUnsecuredEndpoints(sb, path, config, hasHealthChecksPackage, projectName);
        }

        sb.AppendLine();
        sb.AppendLine("        return app;");
        sb.AppendLine("    }");
        sb.Append('}');

        return sb.ToString();
    }

    /// <summary>
    /// Generates the health check DI service registration code.
    /// </summary>
    /// <param name="projectName">The project name for namespace.</param>
    /// <returns>Generated C# source code for the service extensions class.</returns>
    public static string GenerateServiceExtensions(string projectName)
    {
        var ns = NamespaceBuilder.BuildBase(projectName);
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");
        sb.AppendLine();
        sb.AppendLine($"namespace {ns}.Health;");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Extension methods for registering health check services.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("public static class HealthCheckServiceExtensions");
        sb.AppendLine("{");
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Adds health check services to the dependency injection container.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static IServiceCollection AddApiHealthChecks(this IServiceCollection services)");
        sb.AppendLine("    {");
        sb.AppendLine("        services.AddHealthChecks();");
        sb.AppendLine("        return services;");
        sb.AppendLine("    }");
        sb.Append('}');

        return sb.ToString();
    }

    private static void AppendUnsecuredEndpoints(
        StringBuilder sb,
        string path,
        HealthCheckConfig config,
        bool hasHealthChecksPackage,
        string projectName)
    {
        if (hasHealthChecksPackage)
        {
            sb.AppendLine($"        app.MapHealthChecks(\"{path}\", HealthCheckOptionsFactory.CreateJson(\"{projectName}\", null));");
        }
        else
        {
            sb.AppendLine($"        app.MapHealthChecks(\"{path}\");");
        }

        if (config.IncludeLiveness)
        {
            AppendHealthCheckMapping(sb, $"\"{path}/live\"", "live", hasHealthChecksPackage, projectName);
        }

        if (config.IncludeReadiness)
        {
            AppendHealthCheckMapping(sb, $"\"{path}/ready\"", "ready", hasHealthChecksPackage, projectName);
        }
    }

    private static void AppendSecuredEndpoints(
        StringBuilder sb,
        string path,
        HealthCheckConfig config,
        bool hasHealthChecksPackage,
        string projectName)
    {
        var queryParam = config.ApiKeyQueryParameterName;
        var headerName = config.ApiKeyHeaderName;

        sb.AppendLine($"        var healthGroup = app.MapGroup(\"{path}\")");
        sb.AppendLine("            .AddEndpointFilter(async (context, next) =>");
        sb.AppendLine("            {");
        sb.AppendLine("                var configuration = context.HttpContext.RequestServices.GetRequiredService<IConfiguration>();");
        sb.AppendLine("                var expectedKey = configuration[\"HealthChecks:ApiKey\"];");
        sb.AppendLine("                if (!string.IsNullOrEmpty(expectedKey))");
        sb.AppendLine("                {");
        sb.AppendLine($"                    var providedKey = context.HttpContext.Request.Query[\"{queryParam}\"].FirstOrDefault()");
        sb.AppendLine($"                        ?? context.HttpContext.Request.Headers[\"{headerName}\"].FirstOrDefault();");
        sb.AppendLine("                    if (!string.Equals(providedKey, expectedKey, StringComparison.Ordinal))");
        sb.AppendLine("                    {");
        sb.AppendLine("                        return Results.Unauthorized();");
        sb.AppendLine("                    }");
        sb.AppendLine("                }");
        sb.AppendLine();
        sb.AppendLine("                return await next(context);");
        sb.AppendLine("            });");
        sb.AppendLine();

        if (hasHealthChecksPackage)
        {
            sb.AppendLine($"        healthGroup.MapHealthChecks(\"/\", HealthCheckOptionsFactory.CreateJson(\"{projectName}\", null));");
        }
        else
        {
            sb.AppendLine("        healthGroup.MapHealthChecks(\"/\");");
        }

        if (config.IncludeLiveness)
        {
            AppendHealthCheckMapping(sb, "\"/live\"", "live", hasHealthChecksPackage, projectName, useGroup: true);
        }

        if (config.IncludeReadiness)
        {
            AppendHealthCheckMapping(sb, "\"/ready\"", "ready", hasHealthChecksPackage, projectName, useGroup: true);
        }
    }

    private static void AppendHealthCheckMapping(
        StringBuilder sb,
        string pathLiteral,
        string tag,
        bool hasHealthChecksPackage,
        string projectName,
        bool useGroup = false)
    {
        var target = useGroup ? "healthGroup" : "app";
        sb.AppendLine();

        if (hasHealthChecksPackage)
        {
            sb.AppendLine($"        {target}.MapHealthChecks({pathLiteral}, new HealthCheckOptions");
            sb.AppendLine("        {");
            sb.AppendLine($"            Predicate = check => check.Tags.Contains(\"{tag}\"),");
            sb.AppendLine($"            ResponseWriter = HealthCheckOptionsFactory.CreateJson(\"{projectName}\", null).ResponseWriter,");
            sb.AppendLine("        });");
        }
        else
        {
            sb.AppendLine($"        {target}.MapHealthChecks({pathLiteral}, new HealthCheckOptions");
            sb.AppendLine("        {");
            sb.AppendLine($"            Predicate = check => check.Tags.Contains(\"{tag}\"),");
            sb.AppendLine("        });");
        }
    }

    private static bool IsApiKeySecurity(HealthCheckConfig config)
        => string.Equals(config.Security, "apiKey", StringComparison.OrdinalIgnoreCase);
}