namespace Atc.Rest.Api.Generator.Extractors;

/// <summary>
/// Generates a Constants class containing shared values for the generated API client.
/// Used in EndpointPerOperation mode to expose the HTTP client name as a typed constant.
/// </summary>
public static class ConstantsExtractor
{
    /// <summary>
    /// Generates the Constants class containing the HttpClientName constant.
    /// </summary>
    /// <param name="projectName">The project name (base namespace).</param>
    /// <param name="effectiveHttpClientName">The resolved HTTP client name.</param>
    /// <returns>The generated C# source code.</returns>
    public static string Generate(
        string projectName,
        string effectiveHttpClientName)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine($"namespace {NamespaceBuilder.BuildBase(projectName)};");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Constants for the generated API client.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("public static class Constants");
        sb.AppendLine("{");
        sb.AppendLine(4, "/// <summary>");
        sb.AppendLine(4, "/// The HTTP client name used by IHttpClientFactory for this API client.");
        sb.AppendLine(4, "/// </summary>");
        sb.AppendLine(4, $"public const string HttpClientName = \"{effectiveHttpClientName}\";");
        sb.AppendLine("}");

        return sb.ToString();
    }
}